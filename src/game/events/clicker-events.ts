import type { GameEvent } from '../events';

function healPart(p: { hp: number; maxHp: number; heal: (n: number) => void }, ratio: number): number {
  const amount = Math.max(1, Math.floor(p.maxHp * ratio));
  p.heal(amount);
  return amount;
}

function healFull(p: { hp: number; maxHp: number }): number {
  const before = p.hp;
  p.hp = p.maxHp;
  return p.maxHp - before;
}

