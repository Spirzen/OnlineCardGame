export const STORY_TUTORIAL_STORAGE_KEY = 'ural_batyr_story_tutorial_status';

export type StoryTutorialStatus = 'none' | 'declined' | 'completed' | 'in_progress';

export function getStoryTutorialStatus(): StoryTutorialStatus {
  try {
    const v = localStorage.getItem(STORY_TUTORIAL_STORAGE_KEY);
    if (v === 'declined' || v === 'completed' || v === 'in_progress') return v;
    return 'none';
  } catch {
    return 'none';
  }
}

export function setStoryTutorialStatus(status: StoryTutorialStatus) {
  try {
    localStorage.setItem(STORY_TUTORIAL_STORAGE_KEY, status);
  } catch {
    /* ignore */
  }
}

export function shouldOfferStoryTutorial(): boolean {
  return getStoryTutorialStatus() === 'none';
}
